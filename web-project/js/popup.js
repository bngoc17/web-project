function login() {
  var x = document.getElementById("panel");
  if (x.style.display === "none") x.style.display = "block"; else x.style.display = "none";
}
function logout() {
  var x = document.getElementById("panel1");
  if (x.style.display === "none") x.style.display = "block"; else x.style.display = "none";
}
function search() {
  var x = document.getElementById("searchbar");
  if (x.style.display === "none") x.style.display = "block"; else x.style.display = "none";
}